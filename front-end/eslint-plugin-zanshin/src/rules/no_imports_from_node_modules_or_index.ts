import { createRule } from './utils';

const prohibitedPaths =
  /^(.*node_modules.*|\/.*|\.\/\.\.\/.*|.*\.|.*\/|.*\/index|.*\/lib|.*\.\/lib\/.*|.*\/lib-commonjs|.*\.\/lib-commonjs\/.*|.*\/src|.*\.\/src\/.*|@zanshin\/[^/]+\/lib\/.*)$/;

/*
prohibit:
    node_modules: .*node_modules.*
    absolute paths: \/.*
    any relative imports that start with ./../: \.\/\.\.\/.*
    any imports that ends with .: .*\.
    any imports that ends with / (i.e. import from index): .*\/
    any imports that ends with index: .*\/index
    any imports that ends with /lib: .*\/lib
    any imports that contains ./lib (i.e. ../lib or ./lib): .*\.\/lib\/.*
    any imports that ends with /lib-commonjs: .*\/lib-commonjs
    any imports that contains ./lib-commonjs (i.e. ../lib-commonjs or ./lib-commonjs): .*\.\/lib-commonjs\/.*
    any imports that contains @zanshin/.../lib (e.g. @zanshin/collection-api/lib/item_ref_queues): @zanshin\/[^/]+\/lib\/.*
*/

export const noImportsFromNodeModulesOrIndex = createRule({
  name: 'no-imports-from-node-modules-or-index',
  meta: {
    type: 'problem',
    docs: {
      description: 'Disallow imports from node_modules or index',
    },
    messages: {
      invalidImportPath:
        'Invalid import path detected: {{ importPath }}\n Importing from node_modules and indices is not allowed.',
    },
    schema: [], // No options,
    fixable: 'code',
  },

  defaultOptions: [],
  create: function (context) {
    return {
      ImportDeclaration(node) {
        if (node.source.value.match(prohibitedPaths)) {
          context.report({
            node,
            messageId: 'invalidImportPath',
            data: { importPath: node.source.value },
          });
        }
      },
    };
  },
});
